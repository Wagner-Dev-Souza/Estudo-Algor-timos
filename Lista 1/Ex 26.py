'''Faça um algoritmo que receba três
números, calcule e mostre a multiplicação desses
números.'''

n1 = float(input('Insira o primeiro número: '))
n2 = float(input('Insira o segundo número: '))
n3 = float(input('Insira o terceiro número: '))

total = n1 * n2 * n3

print('A multiplicação de {} por {} por {} é igual a {}'.format(n1, n2, n3, total))
